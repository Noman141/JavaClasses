package Assignment3;

public class JavaForEachloop {
    public static void main(String[] args) {
        int[] time={9,8,10,12};
        for (int t: time) {
            System.out.println(t);
        }

        String[] Name={"Jahid","Noman","Hasan","Tasfia","Tabassum"};
        for (String t: Name) {
            System.out.println(t);
        }
    }
}
